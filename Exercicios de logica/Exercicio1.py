print('Faça um algoritmo que receba duas notas e calcule a média aritmética')
print('-----------------------------------------------------------------------')

nota1 = float (input('Por favor, digite sua primeira nota: '))
nota2 = float (input('Agora digite a segunda: '))

media = (nota1 + nota2) / 2

print(f'A média das notas {nota1} e {nota2} é {media}')